public class lab_60Interview {
    public static void main(String[] args) {
        char code='c';
        switch (code){
            case 1:
                System.out.println("yo yo ");
                break;
            case 2:
                System.out.println("no no ");
                break;
            default:
                System.out.println("i am king ");
        }
    }
}
