package Ex_01_JavaBasics;

public class Lab16_ArithmeticOperators {
    public static void main(String[] args) {
        int a=50;
        int b=30;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
