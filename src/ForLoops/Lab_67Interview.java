package ForLoops;

public class Lab_67Interview {
    //EvenOdd
    public static void main(String[] args) {
        for (int i = 0; i <10 ; i++) {
            if(i%2==0){
                System.out.println("Even"+i);
            }
            else{
                System.out.println("Odd"+i);
            }
        }
    }
}
